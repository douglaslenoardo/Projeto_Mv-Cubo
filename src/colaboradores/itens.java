package colaboradores;

public class itens {

	private int id;
	private String cpf_colaboradores;
	private String nome;

	public int getid() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getCpf_colaboradores() {
		return cpf_colaboradores;
	}

	public void setCpf_colaboradores(String cpf_colaboradores) {
		this.cpf_colaboradores = cpf_colaboradores;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

}

